package com.model;

/**
 * Represents the genre of a music band.
 * This enum defines the available music genres that can be assigned to a MusicBand.
 * Note: PLACEHOLDER is not a valid genre and should not be used.
 */
public enum MusicGenre {
    /** Placeholder value - not a valid genre */
    PLACEHOLDER,
    /** Psychedelic rock genre */
    PSYCHEDELIC_ROCK,
    /** Math rock genre */
    MATH_ROCK,
    /** Post rock genre */
    POST_ROCK;
}
